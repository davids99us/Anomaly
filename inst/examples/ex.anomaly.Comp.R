date=seq(as.Date("2000-01-01"),as.Date("2000-12-31"),"days")
l=length(date)
values=rnorm(l)+c(rep(-0.5,l/2),rep(0.5,l/2))
Comps=cbind(date,data.table(sapply(rep(l,10),rnorm)))
Comps=melt(Comps,id="date")
o=anomalyComp(date,values,Comps)
plot(date,values)
lines(o$data$time,o$data$fitted,col=2,lwd=1)
print(o$breaks)